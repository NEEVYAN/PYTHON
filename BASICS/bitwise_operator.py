# complement 
print(~12)
ans : -13
# 2's complement of -13=11110011
# complement of 12=11110011
# =============================================

#and
print(12 & 13)
# ans : 12 

# explanation 
# binary(12)=00001100
# binary(13)=00001101
# 00001100 & 00001101 = 00001100 = binary(12)
# =============================================

#or
print(12 | 13)
# ans : 13 

# explanation 
# binary(12)=00001100
# binary(13)=00001101
# 00001100 & 00001101 = 00001101 = binary(13)
# =============================================

# xor 
print(12^13)
ans : 1

# explanation 
# binary(12)=00001100
# binary(13)=00001101
# 00001100 xor 00001101 = 00000001 = binary(1)
# =============================================

