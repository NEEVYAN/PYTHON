# tuple are immutable whose values cannot be changed 
# denoted by parenthesis
# why tuple instead of list?
# since value cannot be changed then iteration is faster in tuple than list 
# ===========================================================================
# TUPLE 
# ===========================================================================
tup=(23,43,56,21,76,78)
print(tup)
# ans : (23, 43, 56, 21, 76, 78)

# tup[2]=45 
# TypeError: 'tuple' object does not support item assignment


# ===========================================================================
# SET 
# ===========================================================================
s={45,76,46,23,90,12,23}
print(s)
# ans : {23, 90, 12, 45, 46, 76}
